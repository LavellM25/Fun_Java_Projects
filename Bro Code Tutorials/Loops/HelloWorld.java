/* Date: 1-20-2025
   Description: Program that prints "hello" 10 times using a while loop.
*/

public class HelloWorld {
    public static void main(String[] args) {
        int count = 0; // Initialize a counter variable

        // While loop that runs until count is less than 10
        while (count < 10) {
            System.out.println("hello"); // Print "hello"
            count++; // Increment the counter
            /* Explanation:
        Initialize the counter (count): The count variable starts at 0.
        Condition (while count < 10): The loop runs as long as count is less than 10.
        Print statement: Inside the loop, "hello" is printed.
        Increment: After printing, count is incremented by 1 using count++.
        Loop termination: Once count reaches 10, the loop exits.
            */
        }
    }
}
