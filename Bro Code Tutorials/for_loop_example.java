public class ForLoopExample {
    public static void main(String[] args) {
        // Loop from 1 to 5
        for (int i = 1; i <= 5 ; i++) { // Initialization: int i = 1 → Start with i equal to 1.
            // Condition: i <= 5 → Keep looping as long as i is less than or equal to 5.
            // Update: i++ → Increment i by 1 in each iteration.
            System.out.println("Number: " + i); // Prints the current value of i during each iteration.
        }
    }
}
